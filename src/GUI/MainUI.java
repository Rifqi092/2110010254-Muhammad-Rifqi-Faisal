    private void BTtabelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTtabelActionPerformed
        try{
//            object out merupakan inisialasi untuk mengolah file export
            BufferedWriter out = new BufferedWriter(new FileWriter("Export.txt"));
            //perulangan yang dimana menghitung c sebagai baris dan v sebagai column
            for(int c = 0; c < TBdiary.getRowCount(); c++){
                for(int v = 0; v < TBdiary.getColumnCount(); v++){
                    //lakukan penulisan berdasar nilai dari c dan v berdasarkan incrementnya
                  out.write(TBdiary.getValueAt(c, v).toString().replace("\n", "\\n")+"/"); 
                }
                //membuat penulisan di baris baru / enter
                out.newLine();
            }
            JOptionPane.showMessageDialog(null, "Berhasil Disimpan dalam File");
            out.close();
        }catch(Exception e){
            System.err.println("Error" + e.getMessage());
        }
    }//GEN-LAST:event_BTtabelActionPerformed

