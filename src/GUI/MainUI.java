    private void BTeditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTeditActionPerformed
        //validasi jika tabel belum diklik
        if (TFid.getText().equals("")){
            JOptionPane.showMessageDialog(
                    null,
                    ", Silahkan Klik dulu tablenya", "Pesan Kesalahan",
                    JOptionPane.ERROR_MESSAGE
            );
        }else{
            //mengambil data dari tbdiary menggunakan objek data
         TableModel data = TBdiary.getModel();
        int row = TBdiary.getSelectedRow(); 
        //konfirmasi untuk mengedit
        int result= JOptionPane.showConfirmDialog(this,"Apakah Anda Yakin Ingin mengubah data "+ data.getValueAt(row, 1).toString(),"Edit",
                JOptionPane.YES_NO_OPTION);
        if (result==0)
        {
        SimpleDateFormat tglf = new SimpleDateFormat("dd MMMM yyyy");
        String date = tglf.format(jDateChooser1.getDate());
        String subject = TFsubject.getText();
        String isi = TAisi.getText();
        
        
        int index = 0;
        //melakukan perulangan dengan variabel p yang isinya dari diaryku
        for(Diary p: diaryku){
            //pengeceka jika ada didalam diaryku ada id yang saman dengan tfid
            if(p.getId() == Integer.parseInt(TFid.getText())){
                index = diaryku.indexOf(p);
                        break;
            }
        }
        //mengset arraylist diaryku pada index yang sudah didapatkan
        diaryku.set(index,new Diary(Integer.parseInt(TFid.getText()),date,subject,isi));
        tampilDiary("");
        reset();
        }
        }
        
    }//GEN-LAST:event_BTeditActionPerformed
