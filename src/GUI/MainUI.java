    //query data
    public void getDataDiary(String key){
        //array list yang dipakai jika sedang mencari
       ArrayList<Diary> listdiary = new ArrayList<Diary>();
       //jika key tidak sama dengan kosong didalam arraylist diaryku tambahkan kedalam listdiary yang mana key sama dengan isi diaryku 
       if(!key.equals("")){
           for(Diary x:diaryku){
               if(x.getSubject().toLowerCase().contains(key.toLowerCase()) || x.getDate().toLowerCase().contains(key.toLowerCase())){
                   listdiary.add(x);
           } 
       }
         //jika tidak samakan isi listdiary dengan diaryku
       }else{
               listdiary = diaryku;
               }
       //membuat objek baru tabel model untuk mengakses tbdiary
        DefaultTableModel dri = (DefaultTableModel)TBdiary.getModel();
        //untuk mmebuat baris dan  4 kolom data 
        Object [] row = new Object[4];
        
        //ini perulangan untuk mendapatkan data dari object listdiary berdasarkan size nya
        for(int i =0; i< listdiary.size(); i++){
            //i = baris dan kolom sudah ditentukan dibawah 0-3 (4) kolom
            row[0] = listdiary.get(i).getId();
            row[1] = listdiary.get(i).getDate();
            row[2] = listdiary.get(i).getSubject();
            row[3] = listdiary.get(i).getIsi();
            
            //membuat baris baru di object dri berdasarkan data dari object row
            dri.addRow(row);
        }
    }
