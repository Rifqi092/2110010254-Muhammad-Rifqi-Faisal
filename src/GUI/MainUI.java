    public final void tampilDiary(String key){
        DefaultTableModel dri = (DefaultTableModel)TBdiary.getModel();
        //setting baris ke 0
        dri.setRowCount(0);
        //lakukan query
        getDataDiary(key);
    }
    //method yang digunakan untuk megakses tanggal di tabel dan membalikkannya menjadi format tanggal
     public Date getDate2(String date){
         SimpleDateFormat tglf = new SimpleDateFormat("dd MMMM yy");
        try{
            Date tgledit = (Date) tglf.parse(date);
            return tgledit;
        }catch (ParseException ex){
            System.err.println("Error Tanggal : "+ex);
            return new Date(System.currentTimeMillis());
        }
    }
     //membuat id otomatis
     public int uniqueId(){
         //megambil index terakhir pada diaryku
         Diary row = diaryku.get(diaryku.size()-1);
         //setelah ketemu maka tambahkan satu  agar tidak menabrak data sebelumya
         int id = row.getId()+1;
         return id;
     }
