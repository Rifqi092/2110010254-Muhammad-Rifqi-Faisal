    private void BTimporActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTimporActionPerformed
        try{
            //membersihkan arraylist
           diaryku.clear();
           //milih file 
            BufferedReader masuk = new BufferedReader(new FileReader("Export.txt"));
            // scan file yang dimaksud
            Scanner data2 = new Scanner(masuk);
            //selama objek scan memilik baris lagi maka 
            while(data2.hasNextLine()){
                //ambil line selanjutnya selagi masih ada
                String row = data2.nextLine();
                // array data yang isinya dipisahka row yang dipisah dengan \
                String[] data = row.split("/");
                String tanggal = data[1];
                String subject = data[2];
                String isi = data[3].replace("\\n", "\n");
                
                diaryku.add(new Diary (Integer.parseInt(data[0]),tanggal,subject,isi));
            }
            JOptionPane.showMessageDialog(null, "berhasil");
        }catch (Exception ex){
            System.err.println("Error"+ex.getMessage());
        }
    }//GEN-LAST:event_BTimporActionPerformed

